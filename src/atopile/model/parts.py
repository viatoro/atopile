def _build_empty_pcb_template() -> str:
    layer_rows = "\n".join(
        f'    ({index} "{name}" {layer_type})'
        for index, name, layer_type in _DEFAULT_PCB_LAYER_TABLE
    )
    return (
        "(kicad_pcb\n"
        "  (version 20221018)\n"
        '  (generator "atopile-web-ide")\n'
        '  (generator_version "0.0.0")\n'
        "  (general)\n"
        '  (paper "A4")\n'
        "  (layers\n"
        f"{layer_rows}\n"
        "  )\n"
        "  (setup)\n"
        '  (net 0 "")\n'
        ")\n"
    )


